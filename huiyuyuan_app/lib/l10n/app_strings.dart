  /// 获取当前语言对应的文本集合
  static Map<String, String> of(AppLanguage language) {
    switch (language) {
      case AppLanguage.zhCN:
        return _zhCN;
      case AppLanguage.en:
        return _en;
      case AppLanguage.zhTW:
        return _zhTW;
    }
  }

  /// 历史兼容字面量 key，逐步迁移中
  static Map<String, String> legacyCompatibilityOf(AppLanguage language) {
    switch (language) {
      case AppLanguage.zhCN:
        return _zhCNLegacyCompatibility;
      case AppLanguage.en:
        return _enLegacyCompatibility;
      case AppLanguage.zhTW:
        return _zhTWLegacyCompatibility;
    }
  }

  /// Legacy-aware lookup kept separate from the primary locale maps.
  static String? lookup(AppLanguage language, String key) {
    return of(language)[key] ??
        legacyCompatibilityOf(language)[key] ??
        of(AppLanguage.zhCN)[key] ??
        legacyCompatibilityOf(AppLanguage.zhCN)[key];
  }

  /// ????????
  static String get(
    AppLanguage language,
    String key, {
    Map<String, Object?> params = const {},
  }) {
    final value = lookup(language, key) ?? key;
    return _interpolate(value, params);
  }

  static String _interpolate(
    String value,
    Map<String, Object?> params,
  ) {
    if (params.isEmpty) {
      return value;
    }

    var result = value;
    for (final entry in params.entries) {
      final replacement = entry.value?.toString() ?? '';
      result = result.replaceAll('{${entry.key}}', replacement);
      result = result.replaceAll('\${${entry.key}}', replacement);
    }
    return result;
  }

